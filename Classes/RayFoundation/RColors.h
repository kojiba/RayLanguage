    #define RBLUE   "\e[1;34m"
    #define RCyan   "\e[0;36m"
    #define RCYAN   "\e[1;36m"
    #define RGreen  "\e[0;32m"
    #define RGREEN  "\e[1;32m"
    #define RYellow "\e[0;33m"
    #define RYELLOW "\e[1;33m"
    #define RNC     "\e[0m"
#else
    #define RRed
    #define RRED
    #define RBlue
    #define RBLUE
    #define RCyan
    #define RCYAN
    #define RGreen
